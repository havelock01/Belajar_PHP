<?php
// *******Pengulangan********

// for
echo "For Conditional : <br>";
for ($i = 0; $i < 5; $i++) {
    echo "Hello world! <br>";
}

// while
echo "While Conditonal : <br>";
$a = 0;
while ($a < 5) {
    echo "Hello world! <br>";
    $a++;
}

// do..while
echo "do...while Condition : <br>";
$b = 0;
do {
    echo "Hello world! <br>";
} while ($b < 5);

// foreach : pengulangan khusus array
?>